export type VbiThemeMode = keyof typeof vbiThemePalettes

export const lightVbiThemeModes = [
  'slate',
  'blue',
  'emerald',
  'amber',
  'rose',
  'violet',
] as const satisfies VbiThemeMode[]
export const darkVbiThemeModes = [
  'zinc',
  'midnight',
  'forest',
  'copper',
  'plum',
  'crimson',
] as const satisfies VbiThemeMode[]
export const isDarkVbiTheme = (mode: VbiThemeMode) =>
  darkVbiThemeModes.includes(mode as (typeof darkVbiThemeModes)[number])
